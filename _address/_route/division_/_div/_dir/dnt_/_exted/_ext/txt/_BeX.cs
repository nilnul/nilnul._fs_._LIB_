namespace nilnul.fs._address._route.division_._div._dir.dnt_._exted._ext.txt
{
	public class Be : nilnul.txt.BeI
	{
		static public bool Be_(string x)
		{

			if (x == "")
			{
				return true;
			}
			else
			{
				//nilnul.bit.vow_.true_.Unacceptable.Singleton.vow( );

				return x.StartsWith(".") && ext_._nonempty.aftDot.Be.Lazy.be(x.Substring(1));
			}

		}

		public bool be(string obj)
		{
			return Be_(obj);
		}


		static public Be Singleton
		{
			get
			{
				return nilnul.obj_.Singleton<Be>.Instance;
			}
		}

	}
}

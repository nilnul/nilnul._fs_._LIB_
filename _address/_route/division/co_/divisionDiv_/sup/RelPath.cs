using nilnul.fs._address.divDivision;
using System;

namespace nilnul.fs._address.division.co_.divisionDiv_.sup
{

	static public class RelPathX
	{
		static public path_.contextual_.DivisionI Division(
			DivisionI divisionSup
			,
			DivI subDiv
		)
		{
			switch (divisionSup)
			{
				case nilnul.fs._address.DivI divSup:
					return		new nilnul.fs.path_.division_.Div(
						nilnul.fs._address.div.duo_.sup._DifX2._ofSupSub(
							divSup,
							subDiv
						)
					);

				case nilnul.fs._address.IDenotation supDenotation:
					return divDenotation.DuctX.Denotation1(subDiv,supDenotation);

				default:
					throw new UnexpectedTypeException();
					break;
			}
		}
	}
}
